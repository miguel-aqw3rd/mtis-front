package com.app.mtis.requestAPI;

public class UrlKeyValue {
    private String key;
    private String value;

    public UrlKeyValue(String key, String value) {
        this.key = key;
        this.value = value;
    }

    public String getKey() {
        return key;
    }

    public String getValue() {
        return value;
    }
}
